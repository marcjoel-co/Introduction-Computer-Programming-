int char_to_index(char c) {
    if (c >= 'A' && c <= 'Z') {
        return c - 'A';  // Uppercase letters
    } else if (c >= 'a' && c <= 'z') {
        return c - 'a' + 26;  // Lowercase letters (after uppercase)
    } else {
        return -1;  // Character not found
    }
}


